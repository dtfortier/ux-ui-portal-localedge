                    <p><span class="inner-heading">Usage: </span>Left navigation symbol</p>
                    <p><span class="inner-heading">Interactions: </span><code>fa-bullhorn, fa-bart-chart-o, fa-th, fa-th-large, fa-picture-o</code> icons represent a parent navigation item. Only parent navigation items have icons, the child items do not. These icons are part of each link as a whole, they do not perform a seperate interaction from the link to its right.  
