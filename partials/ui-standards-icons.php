                    </br><code>fa-caret-down, fa-caret-up</code> are accordian tools used to expand or collapse the navigation bar. When the item is expanded, the arrow points up. When the item is collapsed, the arrow points down--its' default state.</p>
                    <p><span class="inner-heading">Colors: </span>The icons are white, #ffffff. The hover state of the icon does not change colors on hover.</p>
